function checkAnswer1(answer) {
    let feedback = document.getElementById("feedback1");
    let result = document.getElementById("result1");
    if (answer === "Light") {
        feedback.textContent = "Right answer!!! (you deserve a choco)";
        result.style.backgroundColor = "lightgreen";
    } else {
       feedback.textContent = "Wrong answer!!! Try again :p";
       result.style.backgroundColor = "lightcoral"
   }
}
